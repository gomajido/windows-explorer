// Helper to get file icon based on extension
export function getFileIcon(filename: string, isFolder: boolean): string {
  if (isFolder) return icons.folderFilled;
  
  const ext = filename.split(".").pop()?.toLowerCase() || "";
  
  const iconMap: Record<string, string> = {
    // Documents
    txt: icons.fileText,
    md: icons.fileText,
    pdf: icons.filePdf,
    doc: icons.fileWord,
    docx: icons.fileWord,
    
    // Spreadsheets
    xls: icons.fileExcel,
    xlsx: icons.fileExcel,
    csv: icons.fileExcel,
    
    // Images
    jpg: icons.fileImage,
    jpeg: icons.fileImage,
    png: icons.fileImage,
    gif: icons.fileImage,
    svg: icons.fileImage,
    webp: icons.fileImage,
    
    // Code
    js: icons.fileCode,
    ts: icons.fileCode,
    jsx: icons.fileCode,
    tsx: icons.fileCode,
    vue: icons.fileCode,
    html: icons.fileCode,
    css: icons.fileCode,
    json: icons.fileCode,
    
    // Media
    mp3: icons.fileMusic,
    wav: icons.fileMusic,
    mp4: icons.fileVideo,
    avi: icons.fileVideo,
    mov: icons.fileVideo,
    
    // Archives
    zip: icons.fileZip,
    rar: icons.fileZip,
    "7z": icons.fileZip,
    tar: icons.fileZip,
    gz: icons.fileZip,
  };
  
  return iconMap[ext] || icons.file;
}

// Get color class for file type
export function getFileColor(filename: string, isFolder: boolean): string {
  if (isFolder) return "text-amber-500";
  
  const ext = filename.split(".").pop()?.toLowerCase() || "";
  
  const colorMap: Record<string, string> = {
    // Documents
    pdf: "text-red-500",
    doc: "text-blue-600",
    docx: "text-blue-600",
    
    // Spreadsheets
    xls: "text-green-600",
    xlsx: "text-green-600",
    csv: "text-green-600",
    
    // Images
    jpg: "text-purple-500",
    jpeg: "text-purple-500",
    png: "text-purple-500",
    gif: "text-purple-500",
    svg: "text-purple-500",
    
    // Code
    js: "text-yellow-500",
    ts: "text-blue-500",
    vue: "text-emerald-500",
    html: "text-orange-500",
    css: "text-blue-400",
    json: "text-gray-600",
    
    // Media
    mp3: "text-pink-500",
    mp4: "text-indigo-500",
    
    // Archives
    zip: "text-amber-600",
    rar: "text-amber-600",
  };
  
  return colorMap[ext] || "text-gray-500";
}
